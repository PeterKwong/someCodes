                你應該有注意到GIA證書上，有"鑽石螢光"這一項？

                你知道有一些鑽石，在紫外線下個發出螢光的反應，當然不是全部都有？

                對大部分人來說，螢光不是一個大問題，但有一些卻會對它有點而混亂。以下是一般人常問到的問題，而答案是來自GIA一些有經驗的研究人員。
                <h2>什麼是鑽石螢光？</h2>
                鑽石螢光反應，簡單來講，就是一粒鑽石在UV（紫外線）燈下，會發光的一個現像。當好似你紙幣，在照貨幣的UV燈下，會有一個發光水印出現的現像。當鑽石暴露於紫外光燈下，會發出的一種可見光就是螢光反應。

                在GIA評級證書上，螢光反應就是依據它對長波紫外線燈所作出的反應，強度來分級別，而長波紫外線在日光之下能找到。螢光反應一直會持續直致紫外線光源消失。
                <h2>鑽石螢光常見嗎？</h2>
                是常見的。於過去數十年GIA所評級的鑽石，有接近25-35%鑽石都擁有不同程度的螢光反應。但帶螢光反應的鑽石當中，只有10%螢光強度會對鑽石的外觀有影響（例如：強度是在證書顯示（medium）中、（Strong）強、或者是（Very Strong）非常強。

                超過95%的鑽石它們發出的螢光反應，都是藍色的。而藍色是黃色的互補色，絕大部分鑽石都會帶有微黃，而螢光反而會令鑽石看上去白一點，更接近無色。而極少數的鑽石會帶有黃色、白色，或其他顏色的螢光反應。
                <h2>螢光反應對鑽石的外觀有什麼影響？</h2>
                GIA的其中一個研究指出，絕大部分鑽石的螢光強度，都不會對鑽石造成一個可察覺到的影響。GIA對鑽石螢光的研究中亦發現，一般正常人對"有螢光反應" 及"無螢光反應"的鑽石都不會明顯地察覺的到。

                在好多例子當中，觀察者反而會喜歡有中螢光及強螢光的鑽石。在絕少例子中，當鑽石有（extremely Strong)極強螢光，而引致到它外觀出現霧氣（Hazy）及油（ oily）的現象；在送到GIA評級的螢光鑽石，只有少於0.2%有些現象。
                <h2>螢光反應會損害鑽石的結構嗎？</h2>
                不會。一粒有螢光的鑽石及原全沒有螢光反應的鑽石，結構是一樣的。

                在鑽石中有微量元素及結構的移位，都會引致或者減少鑽石的螢光反應。沒有例子是引致鑽石損害及差的情況。
                <h2>我應該買一粒有螢光的鑽石？</h2>
                對於買螢光鑽石是個人的決定，因為鑽石的整體外觀是一定要考慮。其他因為可以比鑽石螢光影響更大，例如鑽石的<a href="http://tingdiamond.hk/education-diamond-grading/4cs/cut/">切割</a>，是在室內或室外，甚至是你衣著及飾品。

